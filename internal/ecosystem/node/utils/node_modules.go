package utils

import (
	"cli/internal/common"
	"log/slog"
	"path/filepath"
)

var CwdWrongProjectDir = common.NewPrintableError("wrong location, please run this from the project folder")

const nodeModulesDirName = "node_modules"

func ContainsNodeModules(path string) bool {
	// initial check to see if the target path is an npm project directory.
	dir := filepath.Join(path, nodeModulesDirName)
	exists, err := common.DirExists(dir)
	if err != nil {
		slog.Error("failed checking node_modules exists", "err", err)
		return false
	}

	if !exists {
		slog.Info("node_modules does not exist", "path", dir)
		return false
	}

	return true
}
