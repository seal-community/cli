package utils

import (
	"testing"

	"bytes"
)

func TestAddSealPrefixInBlob(t *testing.T) {
	originalBlob := []byte{
		0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x0b, // entry count + content len
		0x00, 0x00, 0x03, 0xe8, 0x00, 0x00, 0x00, 0x06, // tag1 + type1
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, // offset1 + count1
		0x00, 0x00, 0x01, 0x0d, 0x00, 0x00, 0x00, 0x06, // tag2 + type2
		0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00, 0x01, // offset2 + count2
		0x74, 0x65, 0x73, 0x74, 0x00, 0xda, 0x39, 0xa3, // data1 + data2
		0xee, 0x5e, 0x6b, 0x4b, 0x0d, 0x32, 0x55, 0xbf,
		0xef, 0x95, 0x60, 0x18, 0x90, 0xaf, 0xd8, 0x07,
		0x09, 0x2e, 0x33, 0x00,
	}

	// removed 269 tag because it is between 200 and 300 and added seal- to name
	expectedBlob := []byte{
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x0a, // entry count + content len
		0x00, 0x00, 0x03, 0xe8, 0x00, 0x00, 0x00, 0x06, // tag1 + type1
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, // offset1 + count1
		0x73, 0x65, 0x61, 0x6c, 0x2d, 0x74, 0x65, 0x73, // data1
		0x74, 0x00,
	}

	resultBlob, _ := addSealPrefixInBlob(originalBlob)
	if !bytes.Equal(resultBlob, expectedBlob) {
		t.Fatalf("expected %v, got %v", expectedBlob, resultBlob)
	}

	header := createHeaderBlob(resultBlob)
	nameEntry := header.getEntry(packageNameTag)
	if string(nameEntry.Content) != "seal-test\x00" {
		t.Fatalf("expected %v to have prefix %v", nameEntry.Content, SealPrefix)
	}
}
